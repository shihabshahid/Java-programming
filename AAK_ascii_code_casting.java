import java.util.Scanner;

public class AAK_ascii_code_casting {

    public static void main(String[] args) {
       System.out.println("--------1 to 9--------");
       for(int i=48;i<=57;i++){
           System.out.print((char)i+" ");
       }
       System.out.println();
       System.out.println("--------A to Z--------");
       for(int i=65;i<=90;i++){
           System.out.print((char)i+" ");
       }
       System.out.println();
       System.out.println("--------a to z--------");
       for(int i=97;i<=122;i++){
           System.out.print((char)i+" ");
       }
       System.out.println();
       
    }  
}
